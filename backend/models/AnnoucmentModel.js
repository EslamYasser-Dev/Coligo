import mongoose from "mongoose";

const AnnoucmentSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const Annoucment = mongoose.model("Annoucment", AnnoucmentSchema);
